import turtle
turtle.setup(640,640)
window = turtle.Screen()
window.bgcolor("pink")
window.title("Turtle Demo")
pen = turtle.Turtle()
pen.pensize(10)

# Draw Circle
pen.penup()
pen.goto(-320, 0)
pen.pendown()
pen.fillcolor("red")
pen.begin_fill()
pen.circle(100)
pen.end_fill()
pen.penup()
pen.goto(-320,40)
pen.pendown()
pen.fillcolor("pink")
pen.begin_fill()
pen.circle(60)
pen.end_fill()

# Draw V
pen.penup()
pen.goto(-200, 200)
pen.pendown()
pen.fillcolor("cyan")
pen.begin_fill()
pen.forward(40)
pen.goto(-120, 50)
pen.goto(-80, 200)
pen.forward(40)
pen.goto(-100, 0)
pen.backward(40)
pen.goto(-200,200)
pen.end_fill()

# Draw -S
pen.penup()
pen.goto(20,200)
pen.pendown()
pen.fillcolor("violet")
pen.begin_fill()
pen.forward(70)
pen.right(180)
pen.circle(20, -180)
pen.backward(20)
pen.right(180)
pen.circle(20, 180)
pen.forward(20)
pen.right(180)
pen.circle(60,-180)
pen.backward(70)
pen.circle(20,-180)
pen.backward(55)
pen.right(180)
pen.circle(22, 180)
pen.forward(20)
pen.right(180)
pen.circle(65, -140)
pen.goto(20,200)
pen.end_fill()

window.exitonclick() # end program